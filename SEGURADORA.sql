CREATE DATABASE SEGURADORA;

-- CRIANDO AS TABELAS
CREATE TABLE CLIENTE (
	CD_CNPJ CHAR(14) PRIMARY KEY,
	NM_RAZAO_SOCIAL VARCHAR(255)
)

CREATE TABLE SEGURADORA (
	CD_CNPJ CHAR(14) PRIMARY KEY,
	NM_RAZAO_SOCIAL VARCHAR(255)
)

CREATE TABLE PLANO (
	CD_PLANO CHAR(10) PRIMARY KEY,
	NM_PLANO VARCHAR(255),
	SG_PLANO CHAR(2) NOT NULL,
	DS_BENEFICIOS VARCHAR(255)
)

CREATE TABLE CONTRATO (
	CD_CONTRATO CHAR(10) PRIMARY KEY,
	DT_ASSINATURA DATE CHECK (DT_ASSINATURA > CURRENT_DATE),
	CD_CNPJ_CLIENTE CHAR(14) FOREIGN KEY REFERENCES CLIENTE(CD_CNPJ),
	CD_CNPJ_SEGURADORA CHAR(14) FOREIGN KEY REFERENCES SEGURADORA(CD_CNPJ)
)

CREATE TABLE APOLICE (
	CD_APOLICE CHAR(10) PRIMARY KEY,
	DT_VALIDADE DATE CHECK (DT_VALIDADE > CURRENT_DATE),
	CD_PLANO CHAR(10) FOREIGN KEY REFERENCES PLANO,
	CD_CONTRATO CHAR(10) FOREIGN KEY REFERENCES CONTRATO
)

CREATE TABLE SEGURADO (
	CD_SEGURADO CHAR(10) PRIMARY KEY,
	NM_SEGURADO VARCHAR(100),
	DT_NASCIMENTO DATE CHECK (DT_NASCIMENTO < CURRENT_DATE),
	CD_APOLICE CHAR(10) FOREIGN KEY REFERENCES APOLICE,
	IC_TITULAR CHAR(1)
)

